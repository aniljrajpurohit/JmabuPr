;EXPERIMENT_3C: WRITE AN ALP TO INTERFACE DAC AND GENERATE TRIANGULAR WAVEFORM USING 89S52
;NAME: ANIL RAJPUROHIT
;ROLL N0: 312046
;BATCH: B2
;DATE OF PERFORMANCE:1/8/2018
 
			ORG 0000H
				
			CLR A           	;INITIALIZING SATART POSITION i.e 00H
	BACK: 	MOV P1,A     		;LOAD DATA OF A INTO PORT 1
			INC A           	;INCREMENT STEP OF A
			CJNE A,#0FFH,BACK 	;COMPARE TO HIGHEST PEAK OF WAVE
			MOV A,#0FFH     	;INITIALIZING HIGHEST VALUE OF WAVE
	HERE: 	MOV P1,A    		;LOAD DATA OF A INTO PORT 1
			DEC A           	;DECREASING STEP OF WAVE
			CJNE A,#00,HERE   	;COMPARE TO LOWEST PEAK OF WAVE
			SJMP BACK         	;CONTINUOUS WAVEFORM
			
			END