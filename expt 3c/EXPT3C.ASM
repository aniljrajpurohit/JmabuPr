;EXPERIMENT_3b: WRITE AN ALP TO INTERFACE DAC0808 AND GENERATE SAWTOOTH WAVEFORM
;NAME: ANIL RAJPUROHIT
;ROLL N0: 312046, BATCH: B2
;DATE OF PERFORMANCE: 1/8/2018
 
			ORG 000H
				
			MOV A,#00H 		;INITIALIZING INTITIAL POSITION OF SAWTOOTH WAVE
		L1:	MOV P1,A   		;LOAD DATA OF A INTO PORT 1
			INC A      		;INCREMENT STEP OF WAVE
			SJMP L1    		;CONTINUOUS WAVEFORM
		
			END